"""
advisor.py — Policy Advisor Orchestrator
==========================================
Implements the TLC → Rego auto-fix loop described in the SafeSagaLLM paper:

  1. Parse pipeline.yaml
  2. Generate Rego from policy section (P_tran + P_cont)
  3. Generate TLA+ spec from agents, execution_edges, allowed_transfers
  4. Run TLC via TLCVerifier
  5. For each violation:
       - Look up fix rule in tlc_to_opa.yaml
       - If auto_fixable: patch PipelineConfig in-memory → go to 2
       - If not fixable:  print guidance and stop
  6. Converge (zero violations) or report failure after max_iterations

Auto-fixable violations:
  DataIsolation       → remove the leaking (sender → receiver) edge from
                        allowed_transfers so the path is no longer in P
  ContentDataIsolation→ add the escaped keyword to sensitive_keywords

Not auto-fixable (structural / liveness):
  AtomicTermination, CompensationCompleteness, ExecutionOrderPreserved, Deadlock

Usage:
    advisor = PolicyAdvisor(tlc_jar="/path/to/tla2tools.jar")
    ok = advisor.run("pipeline.yaml", root_dir=".")
"""

